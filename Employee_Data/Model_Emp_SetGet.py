# Here we will create a Model class for Employee using Setter and Getter

class Employee:

    def setEid(self,Eid):
        self.Eid = Eid
    def getEid(self):
        return self.Eid

    def setName(self,Name):
        self.Name = Name
    def getName(self):
        return self.Name

    def setDept(self,Dept):
        self.Dept = Dept
    def getDept(self):
        return self.Dept

    def setSalary(self,Salary):
        self.Salary = Salary
    def getSalary(self):
        return self.Salary

